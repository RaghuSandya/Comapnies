package Loops;

public class Break {
// Used to break the Loop or Switch statement.break the current flow of the program.In case of inner loop,it breaks only inner
	public static void main(String[] args) {
		for(int i=1;i<=10;i++){
			if(i==5){
				break;
			}
			System.out.println(i);
		}
	}
	
	
}
