package Loops;

public class ForEachLoop {
//Used to traverse array or collection in java.It is easier than simple loop bcz no need to increment value 
//and use subscript notation
	
	public static void main(String[] args) {
		int arr[]={12,20,50,30,07};
		
		for(int i:arr){
			System.out.println(i);
		}
	}
}
