public class StarPattern {
	public static void main(String[] args) {
		int p=0;
		for(int i=0;i<=5;i++){
			for(int k=1;k<=k-1;k++){
				System.out.print("* ");
			}
			for(int j=0;j<=i+p;j++){
				System.out.print("* ");
			}
			System.out.println();
		}
		
	}

}
